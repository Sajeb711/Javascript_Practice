// do while loop হচ্ছে while loop এর আরেকটি version . 

for (let s = 3; s < 15; s++) {
  document.write(" for " + s);
}


let a = 5; 
while(a <= 25){
  document.write(" while "+ a)
  a++
}


let c = 10;
do{
  document.write( " do while " +  c)
  c++
}
while(c <= 30); 

// do while loop লিখার ক্ষেত্রে প্রথমে একটি variable বলে নিতে হবে।  তারপর do keyword টি ব্যবহার করতে হবে।  তারপর দ্বিতীয় ব্র্যাকেট এর মধ্যে variable টি print করতে হবে। তারপর do এর body র মধ্যে update লিখতে হবে। তারপর while keyword এর মধ্যে condition টি লিখতে হবে। 

